    public void openFile() {
        JFileChooser ch = new JFileChooser();
        FileNameExtensionFilter txtfilter = new FileNameExtensionFilter("TXT Files", "txt");
        //ch.setFileFilter(txtfilter);
        int retVal = ch.showOpenDialog(this);
        if (retVal == JFileChooser.APPROVE_OPTION) {
            jTextField1.setText(ch.getSelectedFile() + "");
        }
        file = new File(jTextField1.getText());
    }

    public void saveFile() {
        BufferedWriter writer = null;
        try {
            JFileChooser ch = new JFileChooser();
            int retVal = ch.showSaveDialog(this);
            if (retVal == JFileChooser.APPROVE_OPTION) {
                FileWriter f = new FileWriter(ch.getSelectedFile());
                writer = new BufferedWriter(f);
                writer.append(jTextArea1.getText());
            }
            if (writer != null) {
                writer.close();
            }
        } catch (HeadlessException | IOException ex) {
            System.out.println(ex.getMessage());
        }
    }

