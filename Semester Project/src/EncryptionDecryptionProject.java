    private void DecryptButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DecryptButtonActionPerformed
        // TODO add your handling code here:
        FileReader reader;
        String decrypted = "";
        String k = jTextField2.getText();
        file = new File(jTextField1.getText());
        try {
            reader = new FileReader(file);
            if (k.equals("")) {
                JOptionPane.showMessageDialog(this, "Please, Enter valid key!", "Error", JOptionPane.INFORMATION_MESSAGE);
                throw new Exception();
            }
            int key = Integer.parseInt(k);
            int ch;
            while ((ch = reader.read()) != -1) {
                if (ch == '\n') {
                    decrypted += "\n";
                    continue;
                }
                if (ch == ' ') {
                    decrypted += " ";
                    continue;
                }
                decrypted += (char) (ch - key);
            }
            reader.close();
        } catch (FileNotFoundException e) {
            JOptionPane.showMessageDialog(this, "No file specified!\nChoose file to process.", "Error", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            System.out.println(e.getMessage());
        } finally {
            jTextArea1.setText(decrypted);
        }
    }//GEN-LAST:event_DecryptButtonActionPerformed

